export function getTouristPlaces(filters?: { category?: string; state?: string; city?: string }) {
    let results = touristPlaces

    if (filters?.category) {
        results = results.filter((p) => p.category.toLowerCase() === filters.category?.toLowerCase())
    }
    if (filters?.state) {
        results = results.filter((p) => p.state.toLowerCase() === filters.state?.toLowerCase())
    }
    if (filters?.city) {
        results = results.filter((p) => p.city.toLowerCase() === filters.city?.toLowerCase())
    }

    return results.sort((a, b) => (b.featured ? 1 : 0) - (a.featured ? 1 : 0))
}